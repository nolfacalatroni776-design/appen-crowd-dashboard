export const metricTip = (...keys: string[]) =>
  keys
    .map((key) => {
      const metric = metricDefinitions[key];
      if (!metric) return key;

      return [
        `${metric.name}`,
        `定义：${metric.definition}`,
      ]
        .filter(Boolean)
        .join('\n');
    })
    .join('\n\n');
